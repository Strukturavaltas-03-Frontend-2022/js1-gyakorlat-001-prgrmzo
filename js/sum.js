let summation = (a, b) => {
    if (a === undefined){
        return 0;
    }
    else if (b === undefined){
        return 0;
    }
    else return a + b;
}

let subtraction = (a, b) => {
    if (a === undefined){
        return 0;
    }
    else if (b === undefined){
        return 0;
    }
    else return a - b;
}